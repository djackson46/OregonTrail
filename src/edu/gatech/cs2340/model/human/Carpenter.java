package edu.gatech.cs2340.model.human;

/**
 * Represents a carpenter.
 * 
 * @author The Jetties
 * 
 */
public class Carpenter extends Human {
	/**
	 * 
	 */
	private static final long serialVersionUID = -3322255123503882198L;

	/**
	 * When you create a carpenter, associate a name with him.
	 * 
	 * @param name
	 *            The name of the carpenter.
	 */
	public Carpenter(String name) {
		super(name, 15, 160);
		this.career = "Carpenter";
		this.profession = CARPENTER;
	}
}
