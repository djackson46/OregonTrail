package edu.gatech.cs2340.model;

/**
 * Enumerates a set of items to be used by store v/c, purchasing, and using
 * methods
 * 
 * @author Jon
 * 
 */
public enum ItemType {
	OX("Ox"), AXLE("Axle"), TONGUE("Tongue"), WHEEL("Wheel"), CLOTHES("Clothes"),
			FOOD("Food"), MEDICINE("Medicine"), RIFLE("Rifle"), AMMO("Ammo");
	private String itemName;

	private ItemType(String inname) {
		itemName = inname;
	}

	/**
	 * This getter gets the name of the item.
	 * 
	 * @return the item's name
	 */
	public String getName() {
		return itemName;
	}
}
