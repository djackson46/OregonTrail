package edu.gatech.cs2340.model.item;

public class Clothing extends Item {
	/**
	 * 
	 */
	private static final long serialVersionUID = 282303159992344368L;
	public Clothing(double inprice, double inweight) {
		super(inprice, inweight);
	}
	public Clothing clone() {
		Clothing clothes = new Clothing(this.price, this.weight);
		clothes.count = count;
		return clothes;
	}
}
