package edu.gatech.cs2340.model.item;

/** Represents an axle. **/
public class Axle extends Item {
	/**
	 * 
	 */
	private static final long serialVersionUID = -440083166935326316L;

	/** Creates the axle. **/
	public Axle(double inprice, double inweight) {
		super(inprice, inweight);
	}
	
	public Axle clone() {
		Axle axle = new Axle(this.price, this.weight);
		axle.count = count;
		return axle;
	}
}
